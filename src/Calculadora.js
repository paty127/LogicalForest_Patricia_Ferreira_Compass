export default class Calculadora{

    static soma(a, b){
        return a + b
    }
    static sub (a, b){  
    return a - b
    }
    static multiplica (a, b){  
        return a * b
}
static divide (a, b){  
    return a / b
    }


static raizQuadrada (a){  
    return Math.sqrt(a) % 1=== 0;
    }

static Circunferencia (a){  
    return 2 * Math.PI * a
    }
}